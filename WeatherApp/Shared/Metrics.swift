/// Пространство имён для хранения метрик, используемых в UI.
///
/// Содержит вложенные перечисления для группировки связанных значений:
/// - `Icon`: размеры иконок
/// - `Layout`: отступы, интервалы и другие параметры компоновки
enum Metrics {

    /// Метрики, связанные с отображением иконок.
    enum Icon {
        /// Стандартный размер иконки (ширина и высота), в пунктах.
        static let size = 40.0
    }

    /// Метрики, связанные с компоновкой элементов интерфейса.
    enum Layout {
        /// Расстояние между элементами интерфейса, в пунктах.
        static let spacing = 8.0
    }
}
